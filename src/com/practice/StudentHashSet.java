package com.practice;

public class StudentHashSet {
	//Create a set collection will hold objects of student type.Each student object should have name and studentId
	private String name;
	public int studentId;
	
	public StudentHashSet(String name,int studentId) {
		this.name=name;
		this.studentId=studentId;
	}
	public String getName() {
		return name;
	}
	

}
